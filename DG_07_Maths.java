package com.company;

import java.util.Scanner;       // import scanner to take input from user

public class DG_07_Maths {
    public static void main(String[] args) {
        System.out.println(Math.max(5.78, 5.79));       // find maximum of two values.
        System.out.println(Math.min(7, 11));            // find minimum of two values.
        System.out.println(Math.random());              // print any value randomly ( less than 1.0 )
        System.out.println((int)(Math.random() * 100));     // type casting ( print random int value less than 100)
    }
}
