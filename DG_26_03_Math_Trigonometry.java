package com.scaler;

public class DG_26_03_Math_Trigonometry {
    public static void main(String[] args) {
        final double PI = Math.PI;          // value of pi
        System.out.println(PI);

        System.out.println(Math.sin(PI/6));     // value of sin(30)
        System.out.println(Math.sin(0));

        System.out.println(Math.cos(PI/3));     // value of cos(60)
        System.out.println(Math.cos(0));

        System.out.println(Math.tan(PI/4));     // value of tan(45)
        System.out.println(Math.tan(0));
    }
}
